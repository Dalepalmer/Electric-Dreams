void	list_spec_pros (CHAR_DATA *ch, char *argument)
{ char arg[MAX_STRING_LENGTH];
  char buf[MAX_STRING_LENGTH];
  char buf2[MAX_STRING_LENGTH];
  int i,j;
  int k;
  
  if (!str_cmp(argument,"")) 
    { send_to_char ("No argument given, Will list 20 spec pros starting with 0\n\r",ch);
      k = 0;
    }
  else
    { one_argument (argument,arg);
      k = atoi (arg);
      if (k < 0 || k >= MAX_SPECPRO)
         { send_to_char ("Invalid Starting Point.\n\r",ch);
           return;
         }
    }
  sprintf (buf,"%-10s%-55s%-10s\n\r","Number","Description","Type");
  for (i=0,j=k;i < MAX_SPECPRO; i++,j++)
    {    sprintf (buf2,"Spec Pro %d\n",j);

	 if (j == MAX_SPECPRO) {
         	page_to_char (buf,ch);
         	return;
         	}
      else
          { sprintf (buf2,"%-10d%-55s%-10c\n\r",spec_pro_list[j].spn,spec_pro_list[j].descr,spec_pro_list[j].type);
            strcat(buf,buf2);
          }
    }
  page_to_char (buf,ch);
  sprintf (buf,"There are a total of %d Spec Pros Avalable.\n\r",MAX_SPECPRO); 
  send_to_char (buf,ch);
  return;
}
